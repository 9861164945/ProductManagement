package com.servlet.ProductManagement;

public class CustomerBean {
    private String cfname, clname, cemail, address, cuname, cpword;
    private long cphno;

    public String getCfname() {
        return cfname;
    }

    public void setCfname(String cfname) {
        this.cfname = cfname;
    }

    public String getClname() {
        return clname;
    }

    public void setClname(String clname) {
        this.clname = clname;
    }

    public String getCemail() {
        return cemail;
    }

    public void setCemail(String cemail) {
        this.cemail = cemail;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getCuname() {
        return cuname;
    }

    public void setCuname(String cuname) {
        this.cuname = cuname;
    }

    public String getCpword() {
        return cpword;
    }

    public void setCpword(String cpword) {
        this.cpword = cpword;
    }

    public long getCphno() {
        return cphno;
    }

    public void setCphno(long cphno) {
        this.cphno = cphno;
    }

	
}
